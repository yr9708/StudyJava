package com.test03;

public class MySum {
	
	// overload(ing) : 같은 이름으로 다른 기능
	
	
	
	public int sum(int i) {
		return i+i;
	}
	
	public int sum(int i, int j) {
		return i+j;
	}
	
	public double sum(double d, double f) {
		return d+f;
	}
	
	public int sum(int i, int j, int k) {
		return i+j+k;
	}
	
	
	
	
	
}
